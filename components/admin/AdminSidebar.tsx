import Link from 'next/link';
import { useRouter } from 'next/router';

interface AdminSidebarProps {
  pendingReviewCount?: number;
}

const navLinks = [
  { href: '/admin', label: 'Dashboard', icon: DashboardIcon },
  { href: '/admin/review-queue', label: 'Review Queue', icon: QueueIcon, badgeKey: 'reviews' as const },
  { href: '/admin/physicians', label: 'Physicians', icon: PhysiciansIcon },
];

export default function AdminSidebar({ pendingReviewCount = 0 }: AdminSidebarProps) {
  const router = useRouter();

  function isActive(href: string) {
    if (href === '/admin') return router.pathname === '/admin';
    return router.pathname.startsWith(href);
  }

  return (
    <nav className="flex flex-col gap-1">
      {navLinks.map((link) => {
        const active = isActive(link.href);
        const Icon = link.icon;
        return (
          <Link
            key={link.href}
            href={link.href}
            className={`
              flex items-center gap-3 px-4 py-3 rounded-[8px] text-sm font-dm-sans font-medium transition-colors
              ${active
                ? 'bg-white/15 text-white'
                : 'text-white/60 hover:text-white hover:bg-white/10'
              }
            `}
          >
            <Icon className="w-5 h-5 flex-shrink-0" />
            <span>{link.label}</span>
            {link.badgeKey === 'reviews' && pendingReviewCount > 0 && (
              <span className="ml-auto bg-alert-garnet text-white text-xs font-bold rounded-full min-w-[20px] h-5 flex items-center justify-center px-1.5">
                {pendingReviewCount}
              </span>
            )}
          </Link>
        );
      })}
    </nav>
  );
}

function DashboardIcon({ className }: { className?: string }) {
  return (
    <svg className={className} fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.5}>
      <path strokeLinecap="round" strokeLinejoin="round" d="M3.75 6A2.25 2.25 0 016 3.75h2.25A2.25 2.25 0 0110.5 6v2.25a2.25 2.25 0 01-2.25 2.25H6a2.25 2.25 0 01-2.25-2.25V6zM3.75 15.75A2.25 2.25 0 016 13.5h2.25a2.25 2.25 0 012.25 2.25V18a2.25 2.25 0 01-2.25 2.25H6A2.25 2.25 0 013.75 18v-2.25zM13.5 6a2.25 2.25 0 012.25-2.25H18A2.25 2.25 0 0120.25 6v2.25A2.25 2.25 0 0118 10.5h-2.25a2.25 2.25 0 01-2.25-2.25V6zM13.5 15.75a2.25 2.25 0 012.25-2.25H18a2.25 2.25 0 012.25 2.25V18A2.25 2.25 0 0118 20.25h-2.25A2.25 2.25 0 0113.5 18v-2.25z" />
    </svg>
  );
}

